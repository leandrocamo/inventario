package modelo;

public class PnlUsuarioNuevo {
    private int usuId;
    private int adId;
    private String usuUsuario;
    private String usuEmail;
    private String usuContrasenia;
    private int usuRole;

    public PnlUsuarioNuevo() {
    }

    public int getUsuId() {
        return usuId;
    }

    public void setUsuId(int usuId) {
        this.usuId = usuId;
    }

    public int getAdId() {
        return adId;
    }

    public void setAdId(int adId) {
        this.adId = adId;
    }

    public String getUsuUsuario() {
        return usuUsuario;
    }

    public void setUsuUsuario(String usuUsuario) {
        this.usuUsuario = usuUsuario;
    }

    public String getUsuEmail() {
        return usuEmail;
    }

    public void setUsuEmail(String usuEmail) {
        this.usuEmail = usuEmail;
    }

    public String getUsuContrasenia() {
        return usuContrasenia;
    }

    public void setUsuContrasenia(String usuContrasenia) {
        this.usuContrasenia = usuContrasenia;
    }

    public int getUsuRole() {
        return usuRole;
    }

    public void setUsuRole(int usuRole) {
        this.usuRole = usuRole;
    }
    
    
}
